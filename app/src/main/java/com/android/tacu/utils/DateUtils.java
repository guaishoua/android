    /**
     * 获取 00：00：00 格式的 （最后一个00是毫秒）
     *
     * @param millisUntilFinished 剩余的毫秒数
     * @return
     */
    public static String getCountDownTime(long millisUntilFinished) {
        // 剩余的小时，分钟，秒，毫秒
        long lHr = millisUntilFinished / hr;
        long lMin = (millisUntilFinished - lHr * hr) / min;
        long lSec = (millisUntilFinished - lHr * hr - lMin * min) / sec;
        long lMs = millisUntilFinished - lHr * hr - lMin * min - lSec * sec;
        String strLMin = getTime(lMin);
        String strLSec = getTime(lSec);
        String strLMs = getMs(lMs);
        return strLMin + ":" + strLSec + ":" + strLMs;
    }

    /**
     * 获取 00：00：00 格式的 （最后一个00是秒）
     *
     * @param millisUntilFinished 剩余的毫秒数
     * @return
     */
    public static String getCountDownTime1(long millisUntilFinished) {
        // 剩余的小时，分钟，秒，毫秒
        long lHr = millisUntilFinished / hr;
        long lMin = (millisUntilFinished - lHr * hr) / min;
        long lSec = (millisUntilFinished - lHr * hr - lMin * min) / sec;
        String strHr = getTime(lHr);
        String strLMin = getTime(lMin);
        String strLSec = getTime(lSec);
        return strHr + ":" + strLMin + ":" + strLSec;
    }

    /**
     * 根据毫秒换算成相应单位后是否大于10来返回相应时间
     */
    private static String getTime(long time) {
        return time > 9 ? String.valueOf(time) : "0" + time;
    }

    /**
     * 获取毫秒
     */
    private static String getMs(long time) {
        String strMs = String.valueOf(time);
        return time > 100 ? strMs.substring(0, strMs.length() - 1) : "00";
    }
